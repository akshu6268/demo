public class lecture9 {
    public static void main(String[] args) {
        // main functions
        multipy();
        add();
        subtract();
        divide();
    }
    // user define function
    static void add(){
        System.out.println(2+5);
    }
    static void subtract(){
        System.out.println(2-5);
    }
    static void multipy(){
        System.out.println(2*5);
    }
    static void divide(){
        System.out.println(2/5);
    }
}
